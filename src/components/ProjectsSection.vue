import $ from 'jquery';
import Swiper from 'swiper/bundle';
import 'swiper/css/bundle';

export default {
    name: 'ProjectsSection',
    mounted() {
        $(() => {
            // 初始化 Swiper 實例
            new Swiper('.mySwiper', {
                direction: 'vertical',
                slidesPerView: 3,
                spaceBetween: 0,
                loop: true,
                autoplay: {
                    delay: 0,
                    disableOnInteraction: false,
                },
                speed: 3000,
            });

            new Swiper('.mySwiper2', {
                direction: 'vertical',
                slidesPerView: 4,
                spaceBetween: 0,
                loop: true,
                autoplay: {
                    delay: 0,
                    disableOnInteraction: false,
                },
                speed: 1000,
            });

            new Swiper('.swiper--top', {
                spaceBetween: 0,
                centeredSlides: true,
                speed: 6000,
                autoplay: {
                    delay: 1,
                },
                loop: true,
                slidesPerView: 'auto',
                allowTouchMove: false,
                disableOnInteraction: true,
            });

            new Swiper('.swiper--bottom', {
                spaceBetween: 0,
                centeredSlides: true,
                speed: 6000,
                autoplay: {
                    delay: 1,
                    reverseDirection: true,
                },
                loop: true,
                slidesPerView: 'auto',
                allowTouchMove: false,
                disableOnInteraction: true,
            });

            // 專案卡片的鼠標移動效果
            const card = $('.project-1');
            const maxRotationX = 20; // X 軸最大旋轉角度
            const maxRotationY = 20; // Y 軸最大旋轉角度

            $(document).on('mousemove', function (e) {
                const ax = -($(window).innerWidth() / 2 - e.pageX) / 20;
                const ay = ($(window).innerHeight() / 2 - e.pageY) / 20;
                card.css(
                    'transform',
                    `rotateY(${Math.min(Math.max(ax, -maxRotationX), maxRotationX)}deg) rotateX(${Math.min(Math.max(ay, -maxRotationY), maxRotationY)}deg)`
                );
            });

            // 初始化 iframe 自適應
            function resizeIframe(iframe) {
                iframe.style.height = iframe.contentWindow.document.body.scrollHeight + 'px';
            }

            function initIframeResize() {
                const iframe = document.getElementById('myIframe');
                if (iframe) {
                    iframe.addEventListener('load', () => {
                        resizeIframe(iframe);
                    });

                    window.addEventListener('resize', () => {
                        resizeIframe(iframe);
                    });
                }
            }

            initIframeResize();
        });
    }
};
</script>
